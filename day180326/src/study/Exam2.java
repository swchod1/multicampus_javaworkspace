public class Exam2 {
	
	/// 메소드 규현
	
	 public int add (int a, int b) {
		  int result;
		  
		  result = a + b;

		  return result;
		 }	
	 
	 public int sub (int a, int b) {
		  int result;
		  
		  result = a - b;

		  return result;
		 }
	 
	 public int mul (int a, int b) {
		  int result;
		  
		  result = a * b;

		  return result;
		 }
	 
	 public int div (int a, int b) {
		  int result;
		  
		  result = a / b;

		  return result;
		 }
}
